#!/usr/bin/env node
// Generate TS artifacts from CUE scenarios under packages/ui/schema/.
//
// Emits TWO files from a single cue export:
//   packages/ui/src/generated/fixtures.ts    — scenario.fixtures
//   packages/ui/src/generated/assertions.ts  — scenario.expected
//
// Both are regenerated on every invocation. --check mode diffs both and
// exits 1 if either drifts (a detector that misses one artifact signals
// clean while it rots).
//
// Renamed from gen-fixtures.mjs in fta once assertions joined fixtures as
// a second emitted artifact — "fixtures" was too narrow to describe the
// output. Script still invoked via pnpm run gen:scenarios[:check].
//
// This is the TS-side counterpart to the Go scenariogen in 6zu. CUE is
// the canonical source; runtime code reads only the generated TS.

import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, "..");
const schemaDir = resolve(pkgRoot, "schema");
const outDir = resolve(pkgRoot, "src/generated");
const fixturesPath = resolve(outDir, "fixtures.ts");
const assertionsPath = resolve(outDir, "assertions.ts");

function loadScenarios() {
  const raw = execFileSync("cue", ["export", ".", "--out=json"], {
    cwd: schemaDir,
    encoding: "utf8",
  });
  const parsed = JSON.parse(raw);
  const areas = parsed?.scenarios?.ui ?? {};
  const out = [];
  for (const area of Object.keys(areas).sort()) {
    for (const sc of areas[area] ?? []) {
      out.push({ area, scenario: sc });
    }
  }
  return out;
}

function toConstName(id) {
  if (!/^[a-z][a-z0-9-]*$/.test(id)) {
    throw new Error(`scenario id not in kebab-case: ${id}`);
  }
  return id.toUpperCase().replace(/-/g, "_");
}

function renderFixtures(scenarios) {
  const header = [
    "// @generated by packages/ui/scripts/gen-scenarios.mjs — DO NOT EDIT.",
    "// Source of truth: packages/ui/schema/*.cue",
    "// Regenerate: cd packages/ui && pnpm run gen:scenarios",
    "",
    'import type { BeadsFixture } from "../types.js";',
    "",
    "export type Scenario = {",
    "  id: string;",
    "  area: string;",
    "  component: string;",
    "  tags: readonly string[];",
    "  fixture: BeadsFixture;",
    "};",
    "",
  ];

  const body = [];
  const entries = [];

  for (const { area, scenario } of scenarios) {
    const cname = toConstName(scenario.id);
    body.push(
      `export const ${cname}: BeadsFixture = ${JSON.stringify(
        scenario.fixtures ?? {},
        null,
        2
      )};`,
      ""
    );
    const meta = {
      id: scenario.id,
      area,
      component: scenario.component,
      tags: scenario.tags ?? [],
    };
    entries.push(
      `  ${JSON.stringify(scenario.id)}: { ...${JSON.stringify(
        meta
      )}, fixture: ${cname} },`
    );
  }

  body.push(
    "export const SCENARIOS: Readonly<Record<string, Scenario>> = {",
    ...entries,
    "};",
    "",
    "export const SCENARIO_LIST: readonly Scenario[] = Object.values(SCENARIOS);",
    ""
  );

  return header.concat(body).join("\n");
}

function renderAssertions(scenarios) {
  const header = [
    "// @generated by packages/ui/scripts/gen-scenarios.mjs — DO NOT EDIT.",
    "// Source of truth: packages/ui/schema/*.cue (expected.{contains,excludes,snapshot,playwright})",
    "// Regenerate: cd packages/ui && pnpm run gen:scenarios",
    "",
    "export type PlaywrightAssertion = {",
    "  screenshot?: string;",
    "  locator_text?: Readonly<Record<string, string>>;",
    "};",
    "",
    "export type ScenarioAssertions = {",
    "  contains: readonly string[];",
    "  excludes: readonly string[];",
    "  snapshot?: string;",
    "  playwright?: PlaywrightAssertion;",
    "};",
    "",
  ];

  const body = [];
  const entries = [];

  for (const { scenario } of scenarios) {
    const cname = `${toConstName(scenario.id)}_EXPECTED`;
    const expected = scenario.expected ?? {};
    const value = {
      contains: expected.contains ?? [],
      excludes: expected.excludes ?? [],
    };
    if (typeof expected.snapshot === "string") {
      value.snapshot = expected.snapshot;
    }
    if (expected.playwright && typeof expected.playwright === "object") {
      const pw = {};
      if (typeof expected.playwright.screenshot === "string") {
        pw.screenshot = expected.playwright.screenshot;
      }
      if (
        expected.playwright.locator_text &&
        typeof expected.playwright.locator_text === "object"
      ) {
        pw.locator_text = expected.playwright.locator_text;
      }
      if (Object.keys(pw).length > 0) value.playwright = pw;
    }
    body.push(
      `export const ${cname}: ScenarioAssertions = ${JSON.stringify(
        value,
        null,
        2
      )};`,
      ""
    );
    entries.push(`  ${JSON.stringify(scenario.id)}: ${cname},`);
  }

  body.push(
    "export const ASSERTIONS: Readonly<Record<string, ScenarioAssertions>> = {",
    ...entries,
    "};",
    ""
  );

  return header.concat(body).join("\n");
}

function diffOrWrite(path, rendered, checkOnly) {
  let existing = null;
  try {
    existing = readFileSync(path, "utf8");
  } catch {
    existing = null;
  }
  if (existing === rendered) {
    return { drift: false };
  }
  if (checkOnly) {
    return { drift: true };
  }
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, rendered);
  return { drift: false, wrote: true };
}

function main() {
  const args = new Set(process.argv.slice(2));
  const checkOnly = args.has("--check");

  const scenarios = loadScenarios();
  if (scenarios.length === 0) {
    throw new Error("no scenarios found under schema/scenarios.ui.*");
  }

  const fixturesRendered = renderFixtures(scenarios);
  const assertionsRendered = renderAssertions(scenarios);

  const fixturesResult = diffOrWrite(fixturesPath, fixturesRendered, checkOnly);
  const assertionsResult = diffOrWrite(
    assertionsPath,
    assertionsRendered,
    checkOnly
  );

  if (checkOnly && (fixturesResult.drift || assertionsResult.drift)) {
    const drifted = [];
    if (fixturesResult.drift) drifted.push("fixtures.ts");
    if (assertionsResult.drift) drifted.push("assertions.ts");
    process.stderr.write(
      `${drifted.join(", ")} out of sync with CUE source — run pnpm run gen:scenarios\n`
    );
    process.exit(1);
  }

  const changed = [];
  if (fixturesResult.wrote) changed.push("fixtures.ts");
  if (assertionsResult.wrote) changed.push("assertions.ts");
  if (changed.length === 0) {
    process.stdout.write(
      `fixtures.ts + assertions.ts up to date (${scenarios.length} scenarios)\n`
    );
  } else {
    process.stdout.write(
      `wrote ${changed.join(", ")} (${scenarios.length} scenarios)\n`
    );
  }
}

main();
