#!/usr/bin/env node
// Generate packages/ui/src/generated/fixtures.ts from CUE scenarios.
//
// Flow: `cue export ./packages/ui/schema --out=json` → JSON → one TS file
// with typed named exports (one per scenario id), a keyed map, and a flat
// array. Scenario id kebab-case → SCREAMING_SNAKE_CASE for the named export
// (sidebar-shows-health → SIDEBAR_SHOWS_HEALTH).
//
// Usage:
//   pnpm run gen:fixtures          # regenerate, write if changed
//   pnpm run gen:fixtures:check    # regenerate, exit 1 if output differs
//
// This is the TS-side counterpart to the Go scenariogen in 6zu. CUE is
// the canonical source; runtime code reads only the generated TS.

import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, "..");
const schemaDir = resolve(pkgRoot, "schema");
const outFile = resolve(pkgRoot, "src/generated/fixtures.ts");

function loadScenarios() {
  const raw = execFileSync("cue", ["export", ".", "--out=json"], {
    cwd: schemaDir,
    encoding: "utf8",
  });
  const parsed = JSON.parse(raw);
  const areas = parsed?.scenarios?.ui ?? {};
  const out = [];
  for (const area of Object.keys(areas).sort()) {
    for (const sc of areas[area] ?? []) {
      out.push({ area, scenario: sc });
    }
  }
  return out;
}

function toConstName(id) {
  if (!/^[a-z][a-z0-9-]*$/.test(id)) {
    throw new Error(`scenario id not in kebab-case: ${id}`);
  }
  return id.toUpperCase().replace(/-/g, "_");
}

function render(scenarios) {
  const header = [
    "// @generated by packages/ui/scripts/gen-fixtures.mjs — DO NOT EDIT.",
    "// Source of truth: packages/ui/schema/*.cue",
    "// Regenerate: cd packages/ui && pnpm run gen:fixtures",
    "",
    'import type { BeadsFixture } from "../types.js";',
    "",
    "export type Scenario = {",
    "  id: string;",
    "  area: string;",
    "  component: string;",
    "  tags: readonly string[];",
    "  fixture: BeadsFixture;",
    "};",
    "",
  ];

  const body = [];
  const entries = [];
  const named = [];

  for (const { area, scenario } of scenarios) {
    const cname = toConstName(scenario.id);
    body.push(
      `export const ${cname}: BeadsFixture = ${JSON.stringify(
        scenario.fixtures ?? {},
        null,
        2
      )};`,
      ""
    );
    const meta = {
      id: scenario.id,
      area,
      component: scenario.component,
      tags: scenario.tags ?? [],
    };
    entries.push(
      `  ${JSON.stringify(scenario.id)}: { ...${JSON.stringify(
        meta
      )}, fixture: ${cname} },`
    );
    named.push(cname);
  }

  body.push(
    "export const SCENARIOS: Readonly<Record<string, Scenario>> = {",
    ...entries,
    "};",
    "",
    "export const SCENARIO_LIST: readonly Scenario[] = Object.values(SCENARIOS);",
    ""
  );

  return header.concat(body).join("\n");
}

function main() {
  const args = new Set(process.argv.slice(2));
  const checkOnly = args.has("--check");

  const scenarios = loadScenarios();
  if (scenarios.length === 0) {
    throw new Error("no scenarios found under schema/scenarios.ui.*");
  }
  const rendered = render(scenarios);

  let existing = null;
  try {
    existing = readFileSync(outFile, "utf8");
  } catch {
    existing = null;
  }

  if (existing === rendered) {
    process.stdout.write(
      `fixtures.ts up to date (${scenarios.length} scenarios)\n`
    );
    return;
  }

  if (checkOnly) {
    process.stderr.write(
      "fixtures.ts out of sync with CUE source — run pnpm run gen:fixtures\n"
    );
    process.exit(1);
  }

  mkdirSync(dirname(outFile), { recursive: true });
  writeFileSync(outFile, rendered);
  process.stdout.write(
    `wrote ${outFile} (${scenarios.length} scenarios)\n`
  );
}

main();
